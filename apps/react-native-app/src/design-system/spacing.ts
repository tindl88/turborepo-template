const generateSpacing = (spacingConfig: SpacingConfig): SpacingStyles => {
  const margin = {
    mxAuto: { marginLeft: 'auto', marginRight: 'auto' },
    myAuto: { marginTop: 'auto', marginBottom: 'auto' }
    // FIXME: Fix type
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
  } as any;

  // FIXME: Fix type
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const padding = {} as any;

  Object.keys(spacingConfig).forEach(key => {
    const value = spacingConfig[+key];

    margin[`m${key}`] = { margin: value };
    margin[`mx${key}`] = { marginHorizontal: value };
    margin[`my${key}`] = { marginVertical: value };
    margin[`mt${key}`] = { marginTop: value };
    margin[`mr${key}`] = { marginRight: value };
    margin[`mb${key}`] = { marginBottom: value };
    margin[`ml${key}`] = { marginLeft: value };

    padding[`p${key}`] = { padding: value };
    padding[`px${key}`] = { paddingHorizontal: value };
    padding[`py${key}`] = { paddingVertical: value };
    padding[`pt${key}`] = { paddingTop: value };
    padding[`pr${key}`] = { paddingRight: value };
    padding[`pb${key}`] = { paddingBottom: value };
    padding[`pl${key}`] = { paddingLeft: value };
  });

  return { margin, padding };
};
const spacingStyles = generateSpacing(spacing);

